	{
		$keys = AbCategoryPeer::getFieldNames($keyType);
		$result = array(
			$keys[0] => $this->getId(),
			$keys[1] => $this->getCatName(),
			$keys[2] => $this->getCreatedAt(),
			$keys[3] => $this->getUpdatedAt(),
		);
		return $result;
	}

	
	public function setByName($name, $value, $type = BasePeer::TYPE_PHPNAME)
	{
		$pos = AbCategoryPeer::translateFieldName($name, $type, BasePeer::TYPE_NUM);
		return $this->setByPosition($pos, $value);
	}

	
	public function setByPosition($pos, $value)
	{
		switch($pos) {
			case 0:
				$this->setId($value);
				break;
			case 1:
				$this->setCatName($value);
				break;
			case 2:
				$this->setCreatedAt($value);
				break;
			case 3:
				$this->setUpdatedAt($value);
				break;
		} 	}

	
	public function fromArray($arr, $keyType = BasePeer::TYPE_PHPNAME)
	{
		$keys = AbCategoryPeer::getFieldNames($keyType);

		if (array_key_exists($keys[0], $arr)) $this->setId($arr[$keys[0]]);
		if (array_key_exists($keys[1], $arr)) $this->setCatName($arr[$keys[1]]);
		if (array_key_exists($keys[2], $arr)) $this->setCreatedAt($arr[$keys[2]]);
		if (array_key_exists($keys[3], $arr)) $this->setUpdatedAt($arr[$keys[3]]);
	}

	
	public function buildCriteria()
	{
		$criteria = new Criteria(AbCategoryPeer::DATABASE_NAME);

		if ($this->isColumnModified(AbCategoryPeer::ID)) $criteria->add(AbCategoryPeer::ID, $this->id);
		if ($this->isColumnModified(AbCategoryPeer::CAT_NAME)) $criteria->add(AbCategoryPeer::CAT_NAME, $this->cat_name);
		if ($this->isColumnModified(AbCategoryPeer::CREATED_AT)) $criteria->add(AbCategoryPeer::CREATED_AT, $this->created_at);
		if ($this->isColumnModified(AbCategoryPeer::UPDATED_AT)) $criteria->add(AbCategoryPeer::UPDATED_AT, $this->updated_at);

		return $criteria;
	}

	
	public function buildPkeyCriteria()
	{
		$criteria = new Criteria(AbCategoryPeer::DATABASE_NAME);

		$criteria->add(AbCategoryPeer::ID, $this->id);

		return $criteria;
	}

	
	public function getPrimaryKey()
