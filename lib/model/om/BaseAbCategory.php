		$this->setId($key);
	}

	
	public function copyInto($copyObj, $deepCopy = false)
	{

		$copyObj->setCatName($this->cat_name);

		$copyObj->setCreatedAt($this->created_at);

		$copyObj->setUpdatedAt($this->updated_at);


		if ($deepCopy) {
									$copyObj->setNew(false);

			foreach($this->getAbAddressess() as $relObj) {
				$copyObj->addAbAddresses($relObj->copy($deepCopy));
			}

		} 

		$copyObj->setNew(true);

		$copyObj->setId(NULL); 
	}

	
	public function copy($deepCopy = false)
	{
				$clazz = get_class($this);
		$copyObj = new $clazz();
		$this->copyInto($copyObj, $deepCopy);
		return $copyObj;
	}

	
	public function getPeer()
	{
		if (self::$peer === null) {
			self::$peer = new AbCategoryPeer();
		}
		return self::$peer;
	}

	
	public function initAbAddressess()
	{
		if ($this->collAbAddressess === null) {
			$this->collAbAddressess = array();
		}
	}

	
	public function getAbAddressess($criteria = null, $con = null)
	{
				include_once 'lib/model/om/BaseAbAddressesPeer.php';
		if ($criteria === null) {
			$criteria = new Criteria();
		}
		elseif ($criteria instanceof Criteria)
		{
			$criteria = clone $criteria;
		}

		if ($this->collAbAddressess === null) {
			if ($this->isNew()) {
			   $this->collAbAddressess = array();
			} else {

				$criteria->add(AbAddressesPeer::AB_CATEGORY_ID, $this->getId());

				AbAddressesPeer::addSelectColumns($criteria);
				$this->collAbAddressess = AbAddressesPeer::doSelect($criteria, $con);
			}
		} else {
						if (!$this->isNew()) {
												

				$criteria->add(AbAddressesPeer::AB_CATEGORY_ID, $this->getId());

				AbAddressesPeer::addSelectColumns($criteria);
				if (!isset($this->lastAbAddressesCriteria) || !$this->lastAbAddressesCriteria->equals($criteria)) {
					$this->collAbAddressess = AbAddressesPeer::doSelect($criteria, $con);
				}
			}
		}
		$this->lastAbAddressesCriteria = $criteria;
		return $this->collAbAddressess;
	}

	
	public function countAbAddressess($criteria = null, $distinct = false, $con = null)
	{
				include_once 'lib/model/om/BaseAbAddressesPeer.php';
		if ($criteria === null) {
			$criteria = new Criteria();
		}
		elseif ($criteria instanceof Criteria)
		{
			$criteria = clone $criteria;
		}

		$criteria->add(AbAddressesPeer::AB_CATEGORY_ID, $this->getId());

		return AbAddressesPeer::doCount($criteria, $distinct, $con);
	}

	
	public function addAbAddresses(AbAddresses $l)
	{
		$this->collAbAddressess[] = $l;
		$l->setAbCategory($this);
	}

